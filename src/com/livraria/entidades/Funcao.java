public class Funcao {
    private Integer id;
    private String nome;

    public Funcao(Integer id, String nome) {
        this.id = id;
        this.nome = nome;
    }
    
    public Funcao() {}

    public Integer getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
    
    
    
    
}
